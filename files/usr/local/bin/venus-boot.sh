#!/bin/bash

NODE=$(basename "$1")
NODE_PATH=$1
NODE_BASE=$(dirname "$NODE_PATH")
DATA_BASE="$NODE_PATH/../$NODE-data"

if [ $# -ne 4 ]; then
    echo "Error: four arguments required"
    echo "Usage: vroot method access iface"
    echo "Example: /opt/venus docker rw eth0"
    exit 1
fi

if [ ! -L /sys/class/net/"$4" ]; then
    echo "Error(arg4): The network interface $VRM_IFACE does not exist"
    exit 1
fi

export VRM_IFACE="$4"

function doChroot
{
    exec chroot "$NODE_PATH" /etc/init.d/venus-manager.sh boot
}

function doDocker
{
    exec docker run --rm \
     --privileged \
     --hostname="$NODE" \
     --net=host \
     -e VRM_IFACE="$VRM_IFACE" \
     -v"$NODE_PATH":/opt \
     -v"$NODE_BASE"/"$NODE"-data:/opt/data \
     -v/run/udev/data:/opt/udev \
     --platform linux/arm64/v8 arm64v8/debian \
    sh -c 'chroot /opt /etc/init.d/venus-manager.sh boot'
}

function doNspawn
{
    exec systemd-nspawn -D  "$NODE_PATH" --link-journal=no \
     --bind=/dev/tty0 \
     --bind=/dev/ttyS0 \
     --bind=/dev/ttyUSB0 \
     --bind=/dev/fb0 \
     --bind=/dev/vhci \
     --bind=/dev/input/touchscreen0 \
     --network-macvlan="$VRM_IFACE" \
     --private-network \
     --capability="CAP_NET_ADMIN,CAP_SYS_MODULE,CAP_SYS_RAWIO,CAP_SYS_ADMIN,CAP_SYS_PTRACE" \
    /etc/init.d/venus-manager.sh boot
}

if [ -z "$NODE_PATH" ]; then
    echo "Error (arg1): A venus root directory is required as an argument"
    exit 1
fi

if [ -z "$2" ]; then
    echo "Error (arg2): A venus boot method (chroot or systemd-nspawn) is required as an argument"
    exit 1
fi

if [ "$3" = "ro" ]; then
    echo "Info: (arg3) Venus root directory will be mounted read only"
    RO="yes"
else
    RO="no"
fi

case "$2" in
    chroot)
        BOOTM=doChroot
    ;;
    systemd-nspawn)
        BOOTM=doNspawn
    ;;
    docker)
        BOOTM=doDocker
    ;;
    *)
    echo "Error (arg2): $2 is not a vlaid boot method"
    exit 1
    ;;
esac

if [ ! -d "$NODE_PATH"/service ]; then
    echo "Error: (arg1) $1 not a directory root for venus"
    exit 1
fi

if [ -d "$NODE_PATH"/run/dbus ]; then
    echo "Already running. Can't be restarted, Please reboot the system"
    exit 1
fi

rm -f /run/"$NODE"-root

umount "$NODE_PATH"/lib/modules &>/dev/null

if ! mkdir -p "$NODE_PATH"/lib/overlay/upper "$NODE_PATH"/lib/overlay/work; then
    echo "Error: failed to create folders for overlayfs"
    exit 1
fi

mount -o,remount,rw "$NODE_PATH" &>/dev/null

if [ "$RO" = "yes" ]; then
   if ! mount -o,remount,ro "$NODE_PATH" &>/dev/null; then
       mount -B -o,ro "$NODE_PATH" "$NODE_PATH"
   fi
fi

UPPER="/tmp/$NODE/upper"
WORK="/tmp/$NODE/work"
LOWER="/lib/modules"
MERGED="$NODE_PATH/lib/modules"

mkdir -p "$UPPER" "$WORK"

if [ -d "$NODE_PATH"/lib/modules ]; then
    if ! mount overlay -t overlay -t overlay -o lowerdir="$LOWER",upperdir="$UPPER",workdir="$WORK" "$MERGED" &>/dev/null; then
        echo "CRITICAL: Failed to overlay-fs to $NODE_PATH/lib/modules"
    fi
fi

if [ ! -d "$DATA_BASE" ]; then
    mkdir "$DATA_BASE" || exit
    echo "Populating venus data folder at $DATA_BASE"
    archive=$(grep --text --line-number 'ARCHIVE:$' $0 | cut -d: -f1)
    tail -n +$((archive + 1)) $0 | base64 -d | tar  xf - -C $DATA_BASE || exit
    sed -r 's/://g' /sys/class/net/"$VRM_IFACE"/address > "$DATA_BASE/venus/unique-id"
fi

if [ -d "$NODE_PATH"/udev ] && [ -d /run/udev/data ]; then

    echo  "$NODE_PATH" >/run/"$NODE"-root

    if test -e "$NODE_PATH"/udev/mp; then
        mount -B /run/udev/data "$NODE_PATH"/data/udev &>/dev/null
    fi

    "$BOOTM" "$NODE_PATH"
fi

# Should not end up here ..
rm -f /run/"$NODE"-root
exit 1

